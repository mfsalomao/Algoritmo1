package lista4; // Pacote ao qual o programa está vinculado.

/**
 * 1. Faça um algoritmo que gere e imprima uma lista com os números de 
 * 1 a 10 em ordem crescente.
 * 
 * @author Martinho de Freitas Salomão
 */
public class Exerc1 { // Início da classe.
    public static void main(String[] args) { // Início do método principal.
        // Início do laço for. Declara a variável contador, inicializando-a em 1
        // e incrementando-a em 1 unidade até que atinja 10.
        for (int contador=1;contador <= 10;contador++) {
            System.out.println(contador); // Saída para o usuário.
        } // Fim do laço for.
    } // Fim do método principal.    
} // Fim da classe.
